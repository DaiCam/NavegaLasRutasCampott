export const getProductos = () => {
  let error = false
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      if (error) {
        reject("Algo salio mal!")
      } else {
        resolve(productos)
      }
    }, 2000)
  })
}

export const getOneProduct = (id) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      let prod = productos.find((producto) => producto.id === id)
      resolve(prod)
    }, 2000)
  })
}
